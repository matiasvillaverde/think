import Foundation

/// Constants for source view display components in message bubbles
public enum SourceViewConstants {
    /// Animation duration for UI changes
    static let animationDuration: Double = 0.3
    /// Scale factor for images
    static let imageScaleFactor: CGFloat = 1.0
    /// Icon size for favicons
    static let iconSize: CGFloat = 24.0
    /// Button text font size
    static let buttonFontSize: CGFloat = 14.0
    /// Horizontal padding for buttons
    static let buttonHorizontalPadding: CGFloat = 12.0
    /// Vertical padding for buttons
    static let buttonVerticalPadding: CGFloat = 6.0
    /// Corner radius for buttons
    static let buttonCornerRadius: CGFloat = 16.0
    /// Spacing between icon and text in buttons
    static let buttonIconSpacing: CGFloat = 4.0
    /// Spacing between elements in row view
    static let rowViewSpacing: CGFloat = 12.0
    /// Vertical padding for row items
    static let rowVerticalPadding: CGFloat = 8.0
    /// Network request timeout interval
    static let requestTimeoutInterval: TimeInterval = 5.0
    /// Maximum lines for description text
    static let descriptionMaxLines: Int = 2
    /// Maximum lines for title text
    static let titleMaxLines: Int = 1
}
